  <div class="details">
    <h4>AG - Streitschlichter</h4>
    <p>Streit und Konflikte zwischen Schülerinnen und Schülern gehören zum Alltag. Auseinandersetzungen werden meist zu Hause durch Eltern oder an der Schule durch Lehrer geregelt. Wir an der Max-Beckmann Grundschule möchten einen zusätzlichen Weg der Konfliktbewältigung gehen: Die Schüler sollen selbst bei Konflikten untereinander vermitteln und zunehmend mehr Eigenverantwortung übernehmen.</p>
    <h3>Streitschlichtung</h3>
    <p>ist ein Beitrag zu einem positiven Schulklima. Schülerinnen und Schüler machen die Erfahrung, dass Gespräche etwas bewirken und Konflikte ohne körperliche Gewalt gelöst werden können. Elf Schülerinnen und Schüler der 3. Klassen freuen sich darüber, dass sie die anspruchsvolle Ausbildung zur Streitschlichterin/zum Streitschlichter geschafft haben. Im Rahmen einer Arbeitsgemeinschaft konnten die Schüler zusammen mit ihrer Ausbilderin Frau Roth die Grundregeln konstruktiver Konfliktlösung aktiv einüben. </p>
    <p>Sie stehen in diesem Schuljahr ihren Mitschülern als Schlichter in Streitsituatio-nen zur Verfügung. Im 2. Halbjahr werden kommende Streitschlichter (der momentanen 3. Klassen) für das nächste Schuljahr wieder neu ausgebildet.</p>

    <h3>Wie funktioniert Streitschlichtung?</h3>

	<p>Bei der Streitschlichtung geht es nicht darum, Standpauken zu halten. Es lässt sich einfacher auf Augenhöhe reden. Deshalb sind die Streitschlichter Schüler, die anderen Schülern in brenzligen Situationen helfen. Sie empfangen die beiden streitenden Parteien zu einem Schlichtungsgespräch.                                     Zu Beginn werden die Regeln der Streitschlichtung klar gemacht. Das Ziel heißt: Gemeinsam eine Lösung finden. Um das zu gewährleisten, sind die Streitschlichter/-innen neutral. Eine weitere Grundregel: Jeder darf seine Meinung sagen und ausreden. Sind die Regeln geklärt, geht es darum, den Grund des Streits herauszufinden und schriftlich festzuhalten. Die beiden Streitparteien erzählen nacheinander ihre Sicht der Dinge. Die Schlichter notieren das Gesagte und fassen es mit eigenen Worten zusammen.</p>

	<p>Nach der „Bestandsaufnahme“ suchen die beiden Streitparteien gemeinsam mit den Streitschlichter/-innen nach Lösungsmöglich-keiten. Es werden Vorschläge gemacht, die zunächst nicht bewertet werden. Abschließend wird geklärt, worauf sich die Streitenden einlassen können. Dieses Abkommen wird dann schriftlich festgehalten. 
		Streitschlichter haben Schweigepflicht.</p>
	<p>Die  Jungen und Mädchen müssen zur Bewältigung ihrer Aufgabe hohe soziale Fähigkeiten mitbringen:</p>
	<ul>
	<li>selbst konfliktarmes bzw. –freies Verhalten zeigen</li>
	<li>offen auf andere zugehen können, kontaktfähig sein</li>  
	<li>sich mündlich leicht verständlich ausdrücken können (ausführlich erzählen können, über einen breiten Wortschatz verfügen)</li>
	<li>schriftlich Inhalte angemessen festhalten können (gute Fähigkeiten im Verfassen von Texten haben)</li>
	<li>ernsthaft im Umgang mit der Thematik sein, glaubwürdig eine Schlichtung durchführen können</li>
	</ul>
	<p>Natürlich steht den Streitschlichtern bei ihren wichtigen Aufgaben ihre Ausbil-derin in „schwierigen“ Streitfällen und beim Besprechen von Problemen zur Seite. Die Streitschlichtung findet nicht auf dem Schulhof vor allen anderen Kindern, sondern in einem „Schlichtungsraum“ im Schulgebäude statt. Übrigens werden die Streitschlichter nie allein eingesetzt, sondern immer im Team zu zweit. Bei jeder Streitschlichtung wird ein schriftlicher Vertrag zwischen den Streiten-den aufgestellt und ein Termin für das Nachtreffen vereinbart.</p>

	<p>Liebe Schülerinnen und Schüler der Max-Beckmann Grundschule, falls bei euch einmal ein größerer Konflikt entsteht, den ihr nicht allein lösen könnt, nutzt bitte das Angebot unserer Streitschlichter. Sie stehen ab Dezember 2012 wieder zur Verfügung  und versuchen in Konfliktsituationen gemeinsam mit euch eine Lösung zu finden.</p>	
