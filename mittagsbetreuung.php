								<h3>Freies Spiel</h3>
								<p>Bei uns steht das „Freie Spiel“ im Vordergrund, weil das die Initiative und Kreativität der Kinder fördert und fordert. Wir haben die Erfahrung gemacht, dass die Kinder nach einem anstrengenden Unterrichtstag erst einmal Erholung, Entspannung, freie Aktivitäten und die Kommunikation mit anderen Kindern und den Betreuern suchen und brauchen.</p>

<h4>Entspannung</h4>

<p>Für Kinder, welche Entspannung suchen bietet eine Betreuerin wöchentlich einen Kurs zur Körperwahrnehmung an. Dabei lernen die Kinder durch Yoga- und Entspannungsübungen den bewussten Umgang mit dem eigenen Körper und dessen Bedürfnisse (Bewegung, Ruhe, Hunger, Nähe, Abgrenzung). Durch bewusstes Erkennen, Wahrnehmen und Anerkennen der Gefühle ohne Bewertung wird ihr Selbstbewusstsein und Selbstwertgefühl gesteigert.<br>
Auch die Wahrnehmung von Aggressionen oder Wut kann ihnen helfen mit Konflikt-situationen positiver umzugehen.</p>

<h4>Bewegung</h4>

<p>Wichtig ist uns aber auch der sportliche Ausgleich. So oft es geht dürfen sich die Kinder im Außengelände oder in der Turnhalle bewegen. Hierfür stehen verschiedene Sport- und Spielgeräte zur Verfügung (z.B. Seile, Bälle, Jongliergeräte, Balancebretter, Rollbretter, Tischtennisschläger…)<br> 
Drei Betreuerinnen, eine davon ist Übungsleiterin Leichtathletin, bieten den Kindern regelmäßig Bewegungsspiele an.</p> 

<h4>Kreativität</h4>

<p>Der Jahreskreis wird in die pädagogische Arbeit mit einbezogen. Dann finden für alle Kinder Bastelaktionen statt (z.B. Osternest, Adventskalender, Faschingsmasken, Mutter- und Vatertagsgeschenke etc.) und die Räumlichkeiten werden entsprechend geschmückt.<br> 
Weitere Angebote sind z.B. Experimente, Vorlesestunden, Arbeit mit Ton, Kochen und Backen</p>

<p>Darüber hinaus feiern wir mit jedem Kind seinen Geburtstag. Dafür haben wir mit den Kindern eine Geburtstagswand gestaltet. Alle singen für das Geburtstagskind ein Lied und es erhält ein kleines Geschenk.<br>
Am letzten Tag vor den Sommerferien gibt es ein Fest mit Wettspielen und Pizza.<br>
Geplant ist auch die Verabschiedung der Viertklässer am Schuljahresende in Form eines Ausfluges.</p>


<h4>Hausaufgabenbetreuung</h4>

<p>Jeden Tag zwischen 11.30 Uhr und 15.15Uhr können die Kinder ihre Hausaufgaben in der Mittagsbetreuung erledigen. Die Hausaufgabenbetreuung erfolgt in Absprache mit den Eltern. 
Die Hausaufgaben werden von unseren Mitarbeiterinnen intensiv begleitet und individuell unterstützt. Unser Ziel ist es, die Kinder zu einem selbständigen und konzentrierten Arbeiten anzuleiten.</p>

