								<p>Kinder brauchen feste Abläufe, klare Regeln und Rituale. Sie helfen den Kindern sich zu orientieren, ihre individuellen Möglichkeiten zu entdecken und das soziale Miteinander zu üben. Deshalb erarbeiten wir mit den Kindern gemeinsam verbindliche Gruppenregeln und sprechen mit ihnen über eventuelle Verstöße. In Zukunft sollen auch in regelmäßigen Abständen Kinderkonferenzen stattfinden, in denen die Kinder gezielt über ihre positiven und negativen Erfahrungen sprechen und eigene Vorschläge einbringen können.</p>

